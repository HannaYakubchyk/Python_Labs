a = int(input("Введите целое число от 0 до 7: "))

if a == 0:
    print("ноль")
elif a == 1:
    print("один")
elif a == 2:
    print("два")
elif a == 3:
    print("три")
elif a == 4:
    print("четыре")
elif a == 5:
    print("пять")
elif a == 6:
    print("шесть")
elif a == 7:
    print("семь")
else:
    print("Вы введили число вне диапазона")